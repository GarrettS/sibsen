<p>
	The recipient (clothed) is placed into positions that stretch the body. Elements of Thai Yoga Massage and 
	Jop Sen are often used together in Thai Massage, depending on client needs.
</p>
<p>
    See also: <a href="http://en.wikipedia.org/wiki/Thai_massage">Thai Massage &mdash; Wikipedia</a>
</p>
